    public void Save() throws IOException{
        this.gv.setId(jTF_Id.getText());
        this.gv.setMaGV(jTF_maGV.getText());
        this.gv.setHoTen(jTF_hoTen.getText());
        this.gv.setDiaChi(JTF_diaChi.getText());
        if(jRB_Nam.isSelected()){
            this.gv.setGioiTinh("Nam");
        }
        if(jRB_Nu.isSelected()){
            this.gv.setGioiTinh("Nu");
        }
        this.gv.setKhoa((String) jComboBox1.getSelectedItem());
        File file = new File("D://giangvien.dat");
        FileWriter fw = new FileWriter(file, true);
        BufferedWriter bw = new BufferedWriter(fw);
        bw.write(this.gv.toString());
    }
    
